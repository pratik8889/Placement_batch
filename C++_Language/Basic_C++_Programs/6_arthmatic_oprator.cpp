#include<iostream>

using namespace std ; 
int  main()
{
    
    int a = 5 , b = 5 ;

    cout<< "the process of  + is : " << a + b << endl ; 
    cout<< "the process of  - is : " << a - b << endl ;
    cout<< "the process of  * is : " << a * b << endl ;
    cout<< "the process of  / is : " << a / b << endl ;
    cout<< "the process of   is : " << a % b << endl ;
    cout<< "the process of  a++ is : " << a++  << endl ;
    cout<< "the process of  a-- is : " << a--  << endl ;
    cout<< "the process of  ++a is : " << ++a  << endl ;
    cout<< "the process of  --a is : " << --a  << endl ;

    return 0 ;

}